#!/usr/bin/env node
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const pluginRoot = path.resolve(__dirname, "..");
const sourceDir = path.join(pluginRoot, "agent-sources");
const outputDir = path.join(pluginRoot, ".codex", "agents");

const MODEL_MAP = new Map([
  ["opus", "gpt-5.5"],
  ["sonnet", "gpt-5.5"],
  ["haiku", "gpt-5.5"],
]);

const EFFORT_MAP = new Map([
  ["minimal", "minimal"],
  ["low", "low"],
  ["medium", "medium"],
  ["high", "high"],
  ["xhigh", "xhigh"],
]);

function parseFrontmatter(text, file) {
  const match = text.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!match) {
    throw new Error(`${file}: missing YAML frontmatter`);
  }

  const data = {};
  let currentListKey = null;

  for (const rawLine of match[1].split("\n")) {
    const line = rawLine.trimEnd();
    if (!line.trim()) continue;

    const listItem = line.match(/^\s*-\s+(.+)$/);
    if (listItem && currentListKey) {
      data[currentListKey].push(unquote(listItem[1].trim()));
      continue;
    }

    const pair = line.match(/^([A-Za-z0-9_-]+):(?:\s*(.*))?$/);
    if (!pair) continue;

    const [, key, rawValue = ""] = pair;
    const value = rawValue.trim();

    if (value === "") {
      data[key] = [];
      currentListKey = key;
    } else {
      data[key] = unquote(value);
      currentListKey = null;
    }
  }

  return { data, body: match[2].trimStart() };
}

function unquote(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }
  return value;
}

function claudeFamily(model) {
  const lower = String(model || "").toLowerCase();
  for (const family of MODEL_MAP.keys()) {
    if (lower.includes(family)) return family;
  }
  return null;
}

function normalizeModel(model) {
  const value = String(model || "gpt-5.5").trim();
  const family = claudeFamily(value);
  if (family) return MODEL_MAP.get(family);
  return MODEL_MAP.get(value) || value;
}

function normalizeEffort(effort, model) {
  const value = String(effort || "medium").trim();
  const normalized = EFFORT_MAP.get(value) || "medium";
  const family = claudeFamily(model);
  if (
    (family === "sonnet" || family === "haiku") &&
    ["minimal", "low"].includes(normalized)
  ) {
    return "medium";
  }
  return normalized;
}

function tomlString(value) {
  return JSON.stringify(String(value));
}

function buildInstructions(body, data, sourceFile) {
  const skillList = Array.isArray(data.skills) ? data.skills : [];
  const skillHint =
    skillList.length > 0
      ? `\n- Prefer these Codex plugin skills when available: ${skillList
          .map((skill) => `codex-elixir-phoenix:${skill}`)
          .join(", ")}.`
      : "";

  const convertedBody = body
    .replaceAll("elixir-phoenix:", "")
    .replaceAll("general-purpose", "worker")
    .replaceAll("From Anthropic research:", "Parallel-agent workflow rationale:");

  return `# Codex Custom Agent: ${data.name}

Converted from upstream agent source: agent-sources/${sourceFile}

## Codex Compatibility Guardrails

This agent was converted from the Claude Elixir/Phoenix plugin for Codex subagents.

- Do not use Claude-only model labels. Use the Codex \`model\` and \`model_reasoning_effort\` fields from this TOML file.
- Treat any Claude \`Agent(...)\` examples below as pseudocode for Codex subagent delegation. In Codex, explicitly spawn available named custom agents, or fall back to built-in \`worker\` / \`explorer\` subagents.
- Do not bypass Codex approvals or sandbox policy. The upstream \`permissionMode\` metadata is intentionally not ported.
- Treat upstream tool names as intent: \`Read\` / \`Grep\` / \`Glob\` mean inspect files; \`Bash\` means run shell commands when allowed; \`Write\` means write only the requested artifact unless the user asked for code edits.${skillHint}
- When a referenced specialist custom agent is unavailable, run that specialist checklist inline or delegate to a focused built-in Codex subagent.

${convertedBody}`;
}

function buildToml({ data, body, sourceFile }) {
  for (const key of ["name", "description"]) {
    if (!data[key]) throw new Error(`${sourceFile}: missing ${key}`);
  }

  const instructions = buildInstructions(body, data, sourceFile);

  return [
    `# Generated by tools/generate-codex-agents.mjs from agent-sources/${sourceFile}.`,
    "# Do not edit by hand; edit the source Markdown and regenerate.",
    "",
    `name = ${tomlString(data.name)}`,
    `description = ${tomlString(data.description)}`,
    `model = ${tomlString(normalizeModel(data.model))}`,
    `model_reasoning_effort = ${tomlString(normalizeEffort(data.effort, data.model))}`,
    `developer_instructions = ${tomlString(instructions)}`,
    "",
  ].join("\n");
}

const files = (await readdir(sourceDir))
  .filter((file) => file.endsWith(".md"))
  .sort();

await mkdir(outputDir, { recursive: true });

for (const file of files) {
  const sourcePath = path.join(sourceDir, file);
  const text = await readFile(sourcePath, "utf8");
  const parsed = parseFrontmatter(text, file);
  const toml = buildToml({ ...parsed, sourceFile: file });
  const outPath = path.join(outputDir, `${parsed.data.name}.toml`);
  await writeFile(outPath, toml, "utf8");
}

console.log(`Generated ${files.length} Codex custom agents in ${path.relative(pluginRoot, outputDir)}`);
