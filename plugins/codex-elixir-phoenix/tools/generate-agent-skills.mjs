#!/usr/bin/env node
import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const pluginRoot = path.resolve(__dirname, "..");
const sourceDir = path.join(pluginRoot, "agent-sources");
const skillsDir = path.join(pluginRoot, "skills");

function parseFrontmatter(text, file) {
  const match = text.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!match) {
    throw new Error(`${file}: missing YAML frontmatter`);
  }

  const data = {};
  let currentListKey = null;

  for (const rawLine of match[1].split("\n")) {
    const line = rawLine.trimEnd();
    if (!line.trim()) continue;

    const listItem = line.match(/^\s*-\s+(.+)$/);
    if (listItem && currentListKey) {
      data[currentListKey].push(unquote(listItem[1].trim()));
      continue;
    }

    const pair = line.match(/^([A-Za-z0-9_-]+):(?:\s*(.*))?$/);
    if (!pair) continue;

    const [, key, rawValue = ""] = pair;
    const value = rawValue.trim();

    if (value === "") {
      data[key] = [];
      currentListKey = key;
    } else {
      data[key] = unquote(value);
      currentListKey = null;
    }
  }

  return data;
}

function unquote(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }
  return value;
}

function titleize(name) {
  const acronyms = new Map([
    ["ash", "Ash"],
    ["ecto", "Ecto"],
    ["otp", "OTP"],
    ["oban", "Oban"],
    ["phoenix", "Phoenix"],
    ["xref", "Xref"],
    ["hex", "Hex"],
  ]);

  return name
    .split("-")
    .map((part) => acronyms.get(part) || part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function shortDescription(description, name) {
  let value = String(description)
    .replace(/\s+Use\s+(proactively\s+)?(when|after|for)\b.*$/i, "")
    .split(" — ")[0]
    .split(" - ")[0]
    .replace(/[.:;,\s]+$/g, "")
    .trim();

  if (!value) value = `${titleize(name)} specialist workflow`;
  if (value.length < 25) value = `${value} specialist workflow`;
  if (value.length > 64) {
    value = value.slice(0, 64).replace(/\s+\S*$/u, "").replace(/[.:;,\s]+$/g, "");
  }
  return value;
}

function yamlString(value) {
  return JSON.stringify(String(value));
}

function skillMarkdown({ name, description, short }) {
  const title = titleize(name);
  return `---
name: ${name}
description: ${yamlString(description)}
metadata:
  short-description: ${yamlString(short)}
---

<!-- Generated by tools/generate-agent-skills.mjs from agent-sources/${name}.md. -->
<!-- Do not edit by hand; edit the source agent Markdown and regenerate. -->

# ${title}

This skill is the Codex plugin entry point for the upstream Claude agent
\`${name}\`.

## Workflow

1. Read \`../../agent-sources/${name}.md\`.
2. Apply the Codex compatibility mappings from \`../codex-compat/SKILL.md\`.
3. Run the specialist workflow inline by default.
4. Use Codex subagents only when the user explicitly asks for subagents,
   delegation, or parallel agent work. If named custom agents are unavailable,
   use a focused built-in \`worker\` or \`explorer\` subagent, or run the same
   checklist inline.

## Output

Follow the output contract in \`../../agent-sources/${name}.md\`, translated for Codex
tool names and project conventions.
`;
}

function openaiYaml({ name, short }) {
  const title = titleize(name);
  return `interface:
  display_name: ${yamlString(title)}
  short_description: ${yamlString(short)}
  default_prompt: ${yamlString(
    `Use $${name} to run the ${title} specialist workflow for this Elixir/Phoenix task.`,
  )}
`;
}

const files = (await readdir(sourceDir))
  .filter((file) => file.endsWith(".md"))
  .sort();

for (const file of files) {
  const sourcePath = path.join(sourceDir, file);
  const text = await readFile(sourcePath, "utf8");
  const data = parseFrontmatter(text, file);

  for (const key of ["name", "description"]) {
    if (!data[key]) throw new Error(`${file}: missing ${key}`);
  }

  const skillRoot = path.join(skillsDir, data.name);
  const agentMetaRoot = path.join(skillRoot, "agents");
  const short = shortDescription(data.description, data.name);

  await mkdir(agentMetaRoot, { recursive: true });
  await writeFile(
    path.join(skillRoot, "SKILL.md"),
    skillMarkdown({ name: data.name, description: data.description, short }),
    "utf8",
  );
  await writeFile(
    path.join(agentMetaRoot, "openai.yaml"),
    openaiYaml({ name: data.name, short }),
    "utf8",
  );
}

console.log(`Generated ${files.length} agent skill wrappers in ${path.relative(pluginRoot, skillsDir)}`);
